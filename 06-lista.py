#6.Algoritmo para criar uma lista usando range com 100 elementos e imprimi-la.

lista = []  #lista vazia que receberá os 100 elementos
i = 0   #contador para iniciar a contagem
for i in range(100):    #verifica se i está na lista
    i += 1  #incrementa um item a lista
    lista.append(i) #relacionada lista e contador
print(lista)    #imprime lista
    

